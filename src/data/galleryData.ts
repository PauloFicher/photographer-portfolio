export const getUniqueCategories = (): string[] => {
  return [...new Set(galleryData.map(img => img.category))].sort();
};

export const getImagesByCategory = (category: string): GalleryImage[] => {
  return galleryData.filter(img => img.category === category);
};

export const getCategoryCounts = (): Record<string, number> => {
  return galleryData.reduce((acc, img) => {
    acc[img.category] = (acc[img.category] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);
};